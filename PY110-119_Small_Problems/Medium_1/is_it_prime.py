# D:
#   Remainders. Only evenly divisible by itself and 1
# A:
#   1. If the input is 1, return False
#   2. Go over the range of the number
#   3. If at any num in thar range
#       A. If the num can be divided into the input number evenly, return it
#   4. If go through the entire range, return False

def is_prime(input_integer):
    if input_integer == 1:
        return False
    for num in range(2, input_integer):
        if input_integer % num == 0:
            return False
    
    return True

